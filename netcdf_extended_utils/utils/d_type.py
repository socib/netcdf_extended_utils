class DType:
    """
    The DType is used to store some names of numpy dtype objects
    """

    # Single-character string
    single_character = 'S1'
    # 8-bit signed integer
    nc_byte = 'B'
    # 64-bit floating point
    nc_double = 'd'
    # 32-bit signed integer
    nc_int = 'i'
